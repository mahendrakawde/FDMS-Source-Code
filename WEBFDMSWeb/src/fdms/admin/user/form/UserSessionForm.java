import org.apache.struts.action.ActionForm;

public class UserSessionForm extends ActionForm {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6295135422446095891L;

	private String id = "0";

	private String sec = "0";

	public void setId(String id) {
		this.id = id;
	}

	public void setSec(String sec) {
		this.sec = sec;
	}

	public String getId() {
		return id;
	}

	public String getSec() {
		return sec;
	}

}
