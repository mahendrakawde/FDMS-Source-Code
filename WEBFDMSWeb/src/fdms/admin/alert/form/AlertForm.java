package fdms.admin.alert.form;

/**
 *
 * @author Guadalupe Garcia
 */
import org.apache.struts.action.ActionForm;

public class AlertForm extends ActionForm {
    
    /**
	 * 
	 */
	private static final long serialVersionUID = 6593885518324590462L;
	
	private String messageType = "M";
    private String message;
    private String viewable = "N";
    private String reboot = "N";
    private String seconds = "0";
    
    public void setMessageType(String messageType) { this.messageType = messageType; }
    public void setMessage(String message) { this.message = message; }
    public void setViewable(String viewable) { this.viewable = viewable; }
    public void setReboot(String reboot) { this.reboot = reboot; }
    public void setSeconds(String seconds) { this.seconds = seconds; }
    
    public String getMessageType() { return messageType; }
    public String getMessage() { return message; }
    public String getViewable() { return viewable; }
    public String getReboot() { return reboot; }
    public String getSeconds() { return seconds; }
}
