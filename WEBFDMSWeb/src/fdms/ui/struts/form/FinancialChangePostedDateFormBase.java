package fdms.ui.struts.form;

import org.apache.struts.action.ActionForm;

public class FinancialChangePostedDateFormBase extends ActionForm {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2597306510288579073L;


	private String fromPostedDate;

	private String toPostedDate;
	private String submitButton;

	public String getSubmitButton() {
		return submitButton;
	}

	public void setSubmitButton(String submitButton) {
		this.submitButton = submitButton;
	}

	public String getFromPostedDate() {
		return fromPostedDate;
	}

	public void setFromPostedDate(String fromPostedDate) {
		this.fromPostedDate = fromPostedDate;
	}

	public String getToPostedDate() {
		return toPostedDate;
	}

	public void setToPostedDate(String toPostedDate) {
		this.toPostedDate = toPostedDate;
	}



}
