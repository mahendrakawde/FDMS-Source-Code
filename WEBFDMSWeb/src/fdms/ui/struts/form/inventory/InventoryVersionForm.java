package fdms.ui.struts.form.inventory;

import java.util.ArrayList;

import org.apache.struts.action.ActionForm;


/**
 * @struts.form name="inventoryVersionForm"
 * 
 */
public class InventoryVersionForm extends ActionForm {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6682221148985305581L;

	private String invVersionID;
	
	private String directive;
	
	private ArrayList versions;
	
	private String activeSelect;

	/**
	 * @return the invVersionID
	 */
	public String getInvVersionID() {
		return invVersionID;
	}

	/**
	 * @param invVersionID the invVersionID to set
	 */
	public void setInvVersionID(String invVersionID) {
		this.invVersionID = invVersionID;
	}

	/**
	 * @return the directive
	 */
	public String getDirective() {
		return directive;
	}

	/**
	 * @return the versions
	 */
	public ArrayList getVersions() {
		return versions;
	}

	/**
	 * @param directive the directive to set
	 */
	public void setDirective(String directive) {
		this.directive = directive;
	}

	/**
	 * @param versions the versions to set
	 */
	public void setVersions(ArrayList versions) {
		this.versions = versions;
	}

	/**
	 * @return the activeSelect
	 */
	public String getActiveSelect() {
		return activeSelect;
	}

	/**
	 * @param activeSelect the activeSelect to set
	 */
	public void setActiveSelect(String activeSelect) {
		this.activeSelect = activeSelect;
	}
	
}