package fdms.ui.struts.form;

import org.apache.struts.action.ActionForm;

public class FinanceChargeFormBase extends ActionForm {
  
  /**
	 * 
	 */
	private static final long serialVersionUID = -6025234851708357969L;
private int x ;  
  private String thruDate ;  
  private String fromDate ;  
  private String directive ;  
  private int y ;
  
  public String getDirective () {
	return directive ;
  }  
  public String getFromDate () {
	return fromDate ;
  }  
  public String getThruDate () {
	return thruDate ;
  }  
  public int getX () {
	return x ;
  }  
  public int getY () {
	return y ;
  }  
  public void setDirective (String in) {
	directive = in;
  }  
  public void setFromDate (String in) {
	fromDate = in;
  }  
  public void setThruDate (String in) {
	thruDate = in;
  }  
  public void setX (int in) {
	x = in;
  }  
  public void setY (int in) {
	y = in;
  }  
}
