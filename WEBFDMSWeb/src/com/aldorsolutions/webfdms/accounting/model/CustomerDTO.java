	private String toQuickbooksXML() {
		StringBuffer sb = new StringBuffer();
		
		sb.append("    <customer>\n");
		sb.append("      <customerid>" + recordNumber + "</customerid>\n");
		sb.append("      <customername>" + FormatString.nullNull(FormatString.escapeXML(firstName)) + " " + 
				FormatString.nullNull(FormatString.escapeXML(lastName)) + "</customername>\n");
		sb.append("      <billaddress>");
		if (streetAddr1.compareTo("") != 0)
			sb.append(FormatString.nullNull(FormatString.escapeXML(streetAddr1)));
		if (streetAddr2.compareTo("") != 0)
			sb.append(" " + FormatString.nullNull(FormatString.escapeXML(streetAddr2)));
		sb.append("</billaddress>\n");
		sb.append("      <billcity>" + FormatString.nullNull(FormatString.escapeXML(city)) + "</billcity>\n");
		sb.append("      <billstate>" + FormatString.nullNull(state) + "</billstate>\n");
		sb.append("      <billzip>" + FormatString.nullNull(zip) + "</billzip>\n");
		sb.append("    </customer>\n");
		return ( sb.toString() ); 
	}
	

