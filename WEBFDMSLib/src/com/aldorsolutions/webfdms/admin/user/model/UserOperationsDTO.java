package com.aldorsolutions.webfdms.admin.user.model;

/**
 * 
 * @author Guadalupe Garcia
 */

import java.io.Serializable;

public class UserOperationsDTO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7929215910109431307L;

	private long userOperationId;
	
	private long userId;
	
	private long operationId;

	public UserOperationsDTO() {
		super();
	}

	public UserOperationsDTO(long userOperationId, long userId, long operationId) {
		super();
		this.userOperationId = userOperationId;
		this.userId = userId;
		this.operationId = operationId;
	}

	/**
	 * @return the operationId
	 */
	public long getOperationId() {
		return operationId;
	}

	/**
	 * @return the userId
	 */
	public long getUserId() {
		return userId;
	}

	/**
	 * @return the userOperationId
	 */
	public long getUserOperationId() {
		return userOperationId;
	}

	/**
	 * @param operationId the operationId to set
	 */
	public void setOperationId(long operationId) {
		this.operationId = operationId;
	}

	/**
	 * @param userId the userId to set
	 */
	public void setUserId(long userId) {
		this.userId = userId;
	}

	/**
	 * @param userOperationId the userOperationId to set
	 */
	public void setUserOperationId(long userOperationId) {
		this.userOperationId = userOperationId;
	}

	
}
