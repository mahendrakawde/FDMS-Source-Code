package com.aldorsolutions.webfdms.ums.model;

public class RolesDTO {
	
	public static final int ROLE_WEBFDMS_USER = 1;
	public static final int ROLE_DASHBOARD_USER = 2;
	public static final int ROLE_DASHBOARD_ACCT = 3;
	public static final int ROLE_DASHBOARD_ADMIN = 4;
	public static final int ROLE_WEBFDMS_PAYMENTS = 5;
	public static final int ROLE_WEBFDMS_ATNEED = 6;
	public static final int ROLE_WEBFDMS_PRENEED = 7;
	public static final int ROLE_WEBFDMS_FINANCIAL = 8;
	public static final int ROLE_WEBFDMS_ACCTS_REC = 9;
	public static final int ROLE_WEBFDMS_FORMS_REPORTS = 10;
	public static final int ROLE_WEBFDMS_DELETE_CASES = 11;
	public static final int ROLE_WEBFDMS_INVENTORY = 12;
	public static final int ROLE_WEBFDMS_SAVE_CHANGES = 13;
	public static final int ROLE_WEBSERVICE_USER = 14;
	public static final int ROLE_WEBSERVICE_ADMIN = 15;
	
	private long roleID;

	private String name;

	private String description;

	private boolean status;

	/**
	 * 
	 */
	public RolesDTO() {
		super();
	}

	/**
	 * @param roleID
	 * @param name
	 * @param description
	 * @param status
	 */
	public RolesDTO(long roleID, String name, String description, boolean status) {
		super();
		this.roleID = roleID;
		this.name = name;
		this.description = description;
		this.status = status;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @return the roleID
	 */
	public long getRoleID() {
		return roleID;
	}

	/**
	 * @return the status
	 */
	public boolean isStatus() {
		return status;
	}

	/**
	 * @param description
	 *            the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @param roleID
	 *            the roleID to set
	 */
	public void setRoleID(long roleID) {
		this.roleID = roleID;
	}

	/**
	 * @param status
	 *            the status to set
	 */
	public void setStatus(boolean status) {
		this.status = status;
	}

}