package com.aldorsolutions.webfdms.arrangers.model;

public class ArrangersFieldDTO {

	private String category;
	private boolean categoryValue;
	private int mainCategory;
	private int subCategory;
	private int displayFields;
	private String alias;
	private int id;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the category
	 */
	public String getCategory() {
		return category;
	}
	/**
	 * @param category the category to set
	 */
	public void setCategory(String category) {
		this.category = category;
	}
	/**
	 * @return the categoryValue
	 */
	public boolean isCategoryValue() {
		return categoryValue;
	}
	/**
	 * @param categoryValue the categoryValue to set
	 */
	public void setCategoryValue(boolean categoryValue) {
		this.categoryValue = categoryValue;
	}
	/**
	 * @return the mainCategory
	 */
	public int getMainCategory() {
		return mainCategory;
	}
	/**
	 * @param mainCategory the mainCategory to set
	 */
	public void setMainCategory(int mainCategory) {
		this.mainCategory = mainCategory;
	}
	/**
	 * @return the subCategory
	 */
	public int getSubCategory() {
		return subCategory;
	}
	/**
	 * @param subCategory the subCategory to set
	 */
	public void setSubCategory(int subCategory) {
		this.subCategory = subCategory;
	}
	/**
	 * @return the displayFields
	 */
	public int getDisplayFields() {
		return displayFields;
	}
	/**
	 * @param displayFields the displayFields to set
	 */
	public void setDisplayFields(int displayFields) {
		this.displayFields = displayFields;
	}
	/**
	 * @return the alias
	 */
	public String getAlias() {
		return alias;
	}
	/**
	 * @param alias the alias to set
	 */
	public void setAlias(String alias) {
		this.alias = alias;
	}
	
	
}
