package com.aldorsolutions.webfdms.beans;

/**
 * A DbFinancialStatementSet represents all financialdata that will be updated
 * to reflect the DateInvoiceSent after the Financial Statements are printed.
 * The peer class provides the class name and SQL for restoring Creation date:
 * (11/26/2002 11:08:33 AM)
 * 
 * @author:
 */
public class DbFinancialStatementSet extends com.aldorsolutions.webfdms.database.PersistentSet {
	final static DbFinancialStatementSetPeer peer = new DbFinancialStatementSetPeer();

	/**
	 * Subclasses must implement this method in order to tell this class what to
	 * use for a PersistentSetPeer.
	 * 
	 * @return the peer for this set
	 */
	protected com.aldorsolutions.webfdms.database.PersistentSetPeer getPersistentSetPeer() {
		return peer;
	}
}
