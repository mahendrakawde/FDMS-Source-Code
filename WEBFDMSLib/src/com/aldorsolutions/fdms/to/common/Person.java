package com.aldorsolutions.fdms.to.common;

import java.util.Date;

public class Person {
	
	private Name name = new Name();
	private String ssn = "";
	private Name otherName = new Name();
	private Date birthDate ;
	private Address birthPlace = Address.getDefault();
	private int ageYears;
	private int ageMonths;
	private int ageDays;
	private int ageHrs;
	private int ageMinutes;
	private String gender = "";
	private Address address = Address.getDefault();
	private String localityOfResidence = "";
	private String residenceLengthOfTime = "";
	private String maritalStatus = "";
	
	public Person(){
		
	}
	
	public Name getName() {
		return name;
	}

	public void setName(Name name) {
		this.name = name;
	}

	public Name getOtherName() {
		return otherName;
	}

	public void setOtherName(Name otherName) {
		this.otherName = otherName;
	}

	public Date getBirthDate() {
		return birthDate;
	}

	public void setBirthDate(Date birthDate) {
		this.birthDate = birthDate;
	}

	public int getAgeYears() {
		return ageYears;
	}

	public void setAgeYears(int ageYears) {
		this.ageYears = ageYears;
	}

	public int getAgeMonths() {
		return ageMonths;
	}

	public void setAgeMonths(int ageMonths) {
		this.ageMonths = ageMonths;
	}

	public int getAgeDays() {
		return ageDays;
	}

	public void setAgeDays(int ageDays) {
		this.ageDays = ageDays;
	}

	public int getAgeHrs() {
		return ageHrs;
	}

	public void setAgeHrs(int ageHrs) {
		this.ageHrs = ageHrs;
	}

	public int getAgeMinutes() {
		return ageMinutes;
	}

	public void setAgeMinutes(int ageMinutes) {
		this.ageMinutes = ageMinutes;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public Address getAddress() {
		return address;
	}

	public void setAddress(Address residence) {
		this.address = residence;
	}

	public String getLocalityOfResidence() {
		return localityOfResidence;
	}

	public void setLocalityOfResidence(String localityOfResidence) {
		this.localityOfResidence = localityOfResidence;
	}

	public Address getBirthPlace() {
		return birthPlace;
	}

	public void setBirthPlace(Address birthPlace) {
		this.birthPlace = birthPlace;
	}

	public String getSSN() {
		return ssn;
	}

	public void setSSN(String ssn) {
		this.ssn = ssn;
	}

	public String getResidenceLengthOfTime() {
		return residenceLengthOfTime;
	}

	public void setResidenceLengthOfTime(String residenceLengthOfTime) {
		this.residenceLengthOfTime = residenceLengthOfTime;
	}

	public String getMaritalStatus() {
		return maritalStatus;
	}

	public void setMaritalStatus(String maritalStatus) {
		this.maritalStatus = maritalStatus;
	}
	
}
