package com.aldorsolutions.fdms.to.service;

import com.aldorsolutions.fdms.to.common.*;
import com.aldorsolutions.fdms.to.fdmscase.*;

public class FdmsAtNeedRequest extends FdmsCaseRequest {

	private FirstCall firstCall;
	private DeceasedPerson deceased;
	private String shippingInfo = "";
	private String callInfoNote = "";
	private RelatedPerson informant = RelatedPerson.getDefault();
	private RelatedPerson nextOfKin = RelatedPerson.getDefault();
	private RelatedPerson executor = RelatedPerson.getDefault();
	
	public FdmsAtNeedRequest(){}

	public String getShippingInfo() {
		return shippingInfo;
	}

	public void setShippingInfo(String shippingInfo) {
		this.shippingInfo = shippingInfo;
	}

	public String getCallInfoNote() {
		return callInfoNote;
	}

	public void setCallInfoNote(String callInfoNote) {
		this.callInfoNote = callInfoNote;
	}

	public FirstCall getFirstCall() {
		return firstCall;
	}

	public void setFirstCall(FirstCall firstCall) {
		this.firstCall = firstCall;
	}

	public DeceasedPerson getDeceased() {
		return deceased;
	}

	public void setDeceased(DeceasedPerson deceased) {
		this.deceased = deceased;
	}

	public RelatedPerson getInformant() {
		return informant;
	}

	public void setInformant(RelatedPerson informant) {
		this.informant = informant;
	}

	public RelatedPerson getNextOfKin() {
		return nextOfKin;
	}

	public void setNextOfKin(RelatedPerson nextOfKin) {
		this.nextOfKin = nextOfKin;
	}

	public RelatedPerson getExecutor() {
		return executor;
	}

	public void setExecutor(RelatedPerson executor) {
		this.executor = executor;
	}
}
