package com.aldorsolutions.fdms.to.vitals;

import java.util.Date;

import com.aldorsolutions.fdms.to.common.*;

public class DispositionInfo {
	
	private Date dateOfNotifyToDirector;
	private String timeOfNotifyToDirector = "";
	private FdmsLocation facility;
	private FdmsArranger director;
	private FdmsArranger embalmer;
	private String methodOfDisposition = "";
	private String dispositionPlace = "";
	private String dispositionPlaceType = "";
	private Address location = Address.getDefault();
	private Date dateEmbalmed;
	private Date dateOfDisposition;
	private String authorizingCoroner = "";

	public DispositionInfo() {}

	public Date getDateOfNotifyToDirector() {
		return dateOfNotifyToDirector;
	}

	public void setDateOfNotifyToDirector(Date dateOfNotifyToDirector) {
		this.dateOfNotifyToDirector = dateOfNotifyToDirector;
	}

	public String getTimeOfNotifyToDirector() {
		return timeOfNotifyToDirector;
	}

	public void setTimeOfNotifyToDirector(String timeOfNotifyToDirector) {
		this.timeOfNotifyToDirector = timeOfNotifyToDirector;
	}

	public FdmsLocation getFacility() {
		return facility;
	}

	public void setFacility(FdmsLocation facility) {
		this.facility = facility;
	}

	public FdmsArranger getDirector() {
		return director;
	}

	public void setDirector(FdmsArranger director) {
		this.director = director;
	}

	public FdmsArranger getEmbalmer() {
		return embalmer;
	}

	public void setEmbalmer(FdmsArranger embalmer) {
		this.embalmer = embalmer;
	}

	public String getMethodOfDisposition() {
		return methodOfDisposition;
	}

	public void setMethodOfDisposition(String methodOfDisposition) {
		this.methodOfDisposition = methodOfDisposition;
	}

	public String getDispositionPlace() {
		return dispositionPlace;
	}

	public void setDispositionPlace(String dispositionPlace) {
		this.dispositionPlace = dispositionPlace;
	}

	public String getDispositionPlaceType() {
		return dispositionPlaceType;
	}

	public void setDispositionPlaceType(String dispositionPlaceType) {
		this.dispositionPlaceType = dispositionPlaceType;
	}

	public Address getLocation() {
		return location;
	}

	public void setLocation(Address location) {
		this.location = location;
	}

	public Date getDateEmbalmed() {
		return dateEmbalmed;
	}

	public void setDateEmbalmed(Date dateEmbalmed) {
		this.dateEmbalmed = dateEmbalmed;
	}

	public Date getDateOfDisposition() {
		return dateOfDisposition;
	}

	public void setDateOfDisposition(Date dateOfDisposition) {
		this.dateOfDisposition = dateOfDisposition;
	}

	public String getAuthorizingCoroner() {
		return authorizingCoroner;
	}

	public void setAuthorizingCoroner(String authorizingCoroner) {
		this.authorizingCoroner = authorizingCoroner;
	}
}
