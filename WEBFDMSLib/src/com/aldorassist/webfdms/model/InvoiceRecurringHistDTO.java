package com.aldorassist.webfdms.model;

import java.sql.Date;

public class InvoiceRecurringHistDTO {

	private int invoiceRecurringHistID;

	private int invoiceID;

	private Date invoiceDate;

	private Date invoiceDueDate;

	private boolean invoicePaid;

	private double amountPaid;

	/**
	 * @return the amountPaid
	 */
	public double getAmountPaid() {
		return amountPaid;
	}

	/**
	 * @param amountPaid
	 *            the amountPaid to set
	 */
	public void setAmountPaid(double amountPaid) {
		this.amountPaid = amountPaid;
	}

	/**
	 * @return the invoiceDate
	 */
	public Date getInvoiceDate() {
		return invoiceDate;
	}

	/**
	 * @param invoiceDate
	 *            the invoiceDate to set
	 */
	public void setInvoiceDate(Date invoiceDate) {
		this.invoiceDate = invoiceDate;
	}

	/**
	 * @return the invoiceDueDate
	 */
	public Date getInvoiceDueDate() {
		return invoiceDueDate;
	}

	/**
	 * @param invoiceDueDate
	 *            the invoiceDueDate to set
	 */
	public void setInvoiceDueDate(Date invoiceDueDate) {
		this.invoiceDueDate = invoiceDueDate;
	}

	/**
	 * @return the invoiceID
	 */
	public int getInvoiceID() {
		return invoiceID;
	}

	/**
	 * @param invoiceID
	 *            the invoiceID to set
	 */
	public void setInvoiceID(int invoiceID) {
		this.invoiceID = invoiceID;
	}

	/**
	 * @return the invoicePaid
	 */
	public boolean isInvoicePaid() {
		return invoicePaid;
	}

	/**
	 * @param invoicePaid
	 *            the invoicePaid to set
	 */
	public void setInvoicePaid(boolean invoicePaid) {
		this.invoicePaid = invoicePaid;
	}

	/**
	 * @return the invoiceRecurringHistID
	 */
	public int getInvoiceRecurringHistID() {
		return invoiceRecurringHistID;
	}

	/**
	 * @param invoiceRecurringHistID
	 *            the invoiceRecurringHistID to set
	 */
	public void setInvoiceRecurringHistID(int invoiceRecurringHistID) {
		this.invoiceRecurringHistID = invoiceRecurringHistID;
	}

}