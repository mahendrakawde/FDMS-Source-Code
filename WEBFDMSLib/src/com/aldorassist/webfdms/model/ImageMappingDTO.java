package com.aldorassist.webfdms.model;

/**
 * 
 * @author Parth
 * 
 * DTO to hold values of PresentationImages & user mapping. 
 *
 */

public class ImageMappingDTO {

	private long imageId;
	private long caseId;
	private long companyId;
	private long presentationImageId;
	private String imageCategory;

	public long getImageId() {
		return imageId;
	}
	public void setImageId(long imageId) {
		this.imageId = imageId;
	}

	public long getCaseId() {
		return caseId;
	}
	public void setCaseId(long caseId) {
		this.caseId = caseId;
	}

	public long getCompanyId() {
		return companyId;
	}
	public void setCompanyId(long companyId) {
		this.companyId = companyId;
	}

	public long getPresentationImageId() {
		return presentationImageId;
	}
	public void setPresentationImageId(long presentationImageId) {
		this.presentationImageId = presentationImageId;
	}

	public String getImageCategory() {
		return imageCategory;
	}
	public void setImageCategory(String imageCategory) {
		this.imageCategory = imageCategory;
	}

}
